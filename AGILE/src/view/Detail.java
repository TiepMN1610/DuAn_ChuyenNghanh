    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        boolean flag = myValidate();
        if (flag) {
            int chu = 0;
            if (cbxChuSoHuu.getSelectedIndex() == 0) {
                chu = 1;
            } else if (cbxChuSoHuu.getSelectedIndex() == 1) {
                chu = 2;
            } else if (cbxChuSoHuu.getSelectedIndex() == 2) {
                chu = 3;
            } else if (cbxChuSoHuu.getSelectedIndex() == 3) {
                chu = 4;
            } else {
                chu = 5;
            }
            int vanPhong = 0;
            if (cbxVanPhong.getSelectedIndex() == 0) {
                vanPhong = 1;
            } else if (cbxVanPhong.getSelectedIndex() == 1) {
                vanPhong = 2;
            } else {
                vanPhong = 3;
            }
            String ma = txtMa.getText();
            String diaChi = txtDiaChi.getText();
            String quan = txtQuan.getText();
            String thanhPho = (String) cbxThanhPho.getSelectedItem();
            String loai = (String) cbxLoai.getSelectedItem();
            int dien = Integer.valueOf(txtDienTich.getText());
            int gia = Integer.valueOf(txtGia.getText());
            BatDongSan bds = new BatDongSan(0, chu, vanPhong, ma, diaChi, quan, thanhPho, loai, dien, gia);
            boolean check = service.updateById(ma, bds);
            if (check) {
                showData(listBatDongSan = service.getData());
                JOptionPane.showMessageDialog(this, "Sửa Thành Công");
                this.dispose();
                s.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Sửa Thất bại");
            }
        }
    }//GEN-LAST:event_btnSuaActionPerformed

