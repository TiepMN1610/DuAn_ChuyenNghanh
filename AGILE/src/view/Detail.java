    public void fill(int index) {
        String check = "";
        if (bds.getChuSoHuu() == 1) {
            check = "Nguyễn Thanh Đức";
        } else if (bds.getChuSoHuu() == 2) {
            check = "Nguyễn Thanh Tuấn";
        } else if (bds.getChuSoHuu() == 3) {
            check = "Nguyễn Thanh Hiệu";
        } else if (bds.getChuSoHuu() == 4) {
            check = "Nguyễn Thanh Nam";
        } else {
            check = "Nguyễn Thanh Tùng";
        }
        String check1 = "";
        if (bds.getVanPhong() == 1) {
            check1 = "Bất động sản Thành Hưng";
        } else if (bds.getVanPhong() == 2) {
            check1 = "Bất động sản Nam Hưng";
        } else {
            check1 = "Bất động sản Tuấn Hưng";
        }
        txtMa.setText(bds.getMa());
        txtDiaChi.setText(bds.getDiaChi());
        txtDienTich.setText(bds.getDienTich() + "");
        txtQuan.setText(bds.getQuan());
        cbxLoai.setSelectedItem(bds.getLoai());
        cbxChuSoHuu.setSelectedItem(check);
        cbxVanPhong.setSelectedItem(check1);
        cbxThanhPho.setSelectedItem(bds.getThanhPho());
        txtGia.setText(bds.getGia() + "");
    }

    public boolean myValidate() {
        boolean flag = false;
        int chon = 0;
        for (int i = 0; i < listBatDongSan.size(); i++) {
            if (listBatDongSan.get(i).equals(txtMa)) {
                chon++;
            }
        }
        if (txtMa.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "không được để trống bất kỳ trường nào");
        } else if (txtQuan.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "không được để trống bất kỳ trường nào");
        } else if (txtDiaChi.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "không được để trống bất kỳ trường nào");
        } else if (txtDienTich.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "không được để trống bất kỳ trường nào");
        } else if (txtGia.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "không được để trống bất kỳ trường nào");
        } else if (!txtGia.getText().matches("[0-9]+")) {
            JOptionPane.showMessageDialog(rootPane, "giá và diện tích phải là số");
        } else if (!txtDienTich.getText().matches("[0-9]+")) {
            JOptionPane.showMessageDialog(rootPane, "giá và diện tích phải là số");
        } else if (chon > 0) {
            JOptionPane.showMessageDialog(rootPane, "không được nhập trùng mã");
        } else {
            flag = true;
        }
        return flag;
    }

